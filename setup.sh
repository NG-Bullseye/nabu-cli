#!/bin/bash
# nabu-cli setup script

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
CONFIG_FILE="$SCRIPT_DIR/config"

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "  nabu-cli Setup"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""

if [ -f "$CONFIG_FILE" ]; then
    echo "✓ Config already exists at $CONFIG_FILE"
    echo "  Delete it and re-run to reconfigure."
    echo ""
else
    echo "Get session IDs from the host:"
    echo "  cat ~/.ha_claude_session   (or check your running nabu/worker)"
    echo ""

    read -rp "Claude Code Session ID for 'nabu': " nabu_sid
    read -rp "Claude Code Session ID for 'worker': " worker_sid
    read -rp "Working directory [$HOME/homeassistant/_data]: " work_dir
    work_dir="${work_dir:-$HOME/homeassistant/_data}"

    cat > "$CONFIG_FILE" << EOF
# nabu-cli Configuration (generated by setup.sh)
NABU_SESSION_ID="${nabu_sid}"
WORKER_SESSION_ID="${worker_sid}"
WORK_DIR="${work_dir}"
EOF

    echo ""
    echo "✓ Config saved to $CONFIG_FILE (git-ignored)"
fi

echo ""
echo "Installing scripts to /usr/local/bin ..."

install_script() {
    local name="$1"
    local tmp
    tmp="$(mktemp)"
    # Bake absolute config path into script
    sed "s|NABU_CONFIG:-\$SCRIPT_DIR/config|NABU_CONFIG:-$CONFIG_FILE|" "$SCRIPT_DIR/$name" > "$tmp"
    sudo cp "$tmp" "/usr/local/bin/$name"
    sudo chmod +x "/usr/local/bin/$name"
    rm -f "$tmp"
    echo "✓ $name → /usr/local/bin/$name"
}

install_script nabu
install_script worker

echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "  Done! Try: nabu"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
